let currentTefila = 'motzash';

function initTefilot() {
  showTefila('motzash');
}

function showTefila(key) {
  currentTefila = key;
  const tf = TEFILOT[key];
  if (!tf) return;

  // Update buttons
  Object.keys(TEFILOT).forEach(k => {
    const btn = document.getElementById(`tf-${k}`);
    if (btn) btn.classList.toggle('active', k === key);
  });

  document.getElementById('tefila-title').textContent  = tf.title;
  document.getElementById('tefila-source').textContent = tf.source;

  const el = document.getElementById('tefila-content');
  el.innerHTML = tf.text.map(line => {
    if (!line) return '<div style="height:12px"></div>';
    // Headers in 【】
    if (line.startsWith('【')) {
      return `<div style="font-size:12px;color:var(--gold);font-weight:700;margin:14px 0 6px;letter-spacing:.5px">${line}</div>`;
    }
    // Targum lines
    if (line.includes('תרגום') || line.includes('יְיָ') || line.includes('מְחֵית')) {
      return `<div style="margin-bottom:8px;color:var(--muted);font-size:calc(var(--font-size) - 1px);line-height:1.8;padding-right:12px;border-right:2px solid var(--border)">${line}</div>`;
    }
    return `<div style="margin-bottom:10px;line-height:1.9">${line}</div>`;
  }).join('');

  updateDoneButton('tefilot', key);
  console.log('[Tefilot] loaded:', key, tf.title);
}
